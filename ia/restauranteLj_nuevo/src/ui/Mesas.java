    public void alternInitComponents(){
        Image restaurante_logo2 = getToolkit().createImage(ClassLoader.getSystemResource("imagenes/restaurante_logo2.png"));
        restaurante_logo2 = restaurante_logo2.getScaledInstance(50, 50, Image.SCALE_SMOOTH);
        this.setIconImage(restaurante_logo2);
        this.setTitle("RESTAURANTE L.J");
        
        
        
        Image foto_usuario_admin = getToolkit().createImage(ClassLoader.getSystemResource("imagenes/admin.png"));
        foto_usuario_admin = foto_usuario_admin.getScaledInstance(50, 50, Image.SCALE_SMOOTH);
        jLabel1.setIcon(new ImageIcon(foto_usuario_admin));
        jLabel3.setText("ROL: "+rol_usuario_activo+" "+nombre_usuario);
        
        if(rol_usuario_activo.equals("cajero")){
            Image foto_usuario_cajero = getToolkit().createImage(ClassLoader.getSystemResource("imagenes/cajero.png"));
            foto_usuario_cajero = foto_usuario_cajero.getScaledInstance(50, 50, Image.SCALE_SMOOTH);
            jLabel1.setIcon(new ImageIcon(foto_usuario_cajero));
            btn_registrar.setEnabled(false);
        }
        Image img_login = getToolkit().createImage(ClassLoader.getSystemResource("imagenes/img_mesas.jpg"));
        img_login = img_login.getScaledInstance(800, 480, Image.SCALE_SMOOTH);
        label_imagen.setIcon(new ImageIcon(img_login));
        
        jPanel1.removeAll();
        PanelMesa temporal = new PanelMesa(data_base,this);
        temporal.setSize(jPanel1.getSize());
        jPanel1.add(temporal);
        repaint();
        revalidate();
        
        
        this.setLocationRelativeTo(null);
        this.setVisible(true);
        this.revalidate();
    }

    public void removerPanel(int numero_mesa){
        this.jPanel1.removeAll();
        PanelMenu temporal = new PanelMenu(data_base,numero_mesa,this);
        temporal.setSize(jPanel1.getSize());
        jPanel1.add(temporal);
        this.repaint();
        this.revalidate();
    }
