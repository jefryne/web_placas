    private void editarUsuario(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editarUsuario

        String c_cedula = this.c_cedula.getText();
        String c_nombre = this.c_nombre.getText();
        String c_apellido = this.c_apellido.getText();
        String c_contrasena = this.c_contrasena.getText();
        String estado = this.c_rol.getText();
        
        boolean respuesta = data_base.editarPersona(c_cedula, c_nombre, c_apellido, c_contrasena,Integer.parseInt(estado));
        if(respuesta){
            l_respusta.setText("Usuario editado");
            this.c_cedula.setText("");
            this.c_nombre.setText("");
            this.c_apellido.setText("");
            this.c_contrasena.setText("");
            this.c_rol.setText("");
        }
    }//GEN-LAST:event_editarUsuario

    private void buscarPersona(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscarPersona
        String cedula = c_cedula.getText();
        System.out.println(cedula);
        ResultSet respusta = data_base.buscarUsuario(cedula);
        System.out.println(respusta);
        if (respusta != null && cedula != null && !"".equals(cedula)){
            try { 
                c_nombre.setText(respusta.getString("nombre"));
                c_apellido.setText(respusta.getString("apellido"));
                c_contrasena.setText(respusta.getString("contrasenia"));
                c_rol.setText(respusta.getString("estado"));
            } catch (SQLException ex) {
                System.out.println("no existe ese campo que buscas en la db");
            }
        }
    }//GEN-LAST:event_buscarPersona

