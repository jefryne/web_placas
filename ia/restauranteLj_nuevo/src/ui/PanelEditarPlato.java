    private void crearPlato(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_crearPlato
       this.removeAll();
       PanelCrearPlato temporal = new PanelCrearPlato(data_base,mesas);
       temporal.setSize(this.getSize());
       this.add(temporal);
       mesas.repaint();
       mesas.revalidate();
    }//GEN-LAST:event_crearPlato

    private void editarPlato(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editarPlato
        String id_plato = c_id_plato.getText();
        String nombre_plato = c_nombre_plato.getText();
        String descripcion_plato = c_descripcion_plato.getText();
        String precio_plato = c_precio_plato.getText();
        boolean respuesta = data_base.editarPlato(Integer.parseInt(id_plato), nombre_plato, descripcion_plato, Integer.parseInt(precio_plato));
        if(respuesta){
            l_respuesta.setText("editado");
            this.c_id_plato.setText("");
            this.c_nombre_plato.setText("");
            this.c_descripcion_plato.setText("");
            this.c_precio_plato.setText("");
        }
    }//GEN-LAST:event_editarPlato

    private void buscarPlato(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscarPlato
        String id_plato = c_id_plato.getText();
        ResultSet respusta = data_base.buscarPlato(Integer.parseInt(id_plato));
        if (respusta != null && id_plato != null && !"".equals(id_plato)){
            try { 
                c_id_plato.setText(respusta.getString("id_plato"));
                c_nombre_plato.setText(respusta.getString("nombre"));
                c_descripcion_plato.setText(respusta.getString("descripcion"));
                c_precio_plato.setText(respusta.getString("precio"));
                l_btn_editar.enableInputMethods(true);
            } catch (SQLException ex) {
                System.out.println("no existe ese campo que buscas en la db");
            }
        }
    }//GEN-LAST:event_buscarPlato
    
    
